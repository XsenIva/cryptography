
# Задаем параметры эллиптической кривой
a = 2   # коэффициент a
b = 3   # коэффициент b
p = 97   # простое число (модуль)

# Создаем конечное поле и эллиптическую кривую
F = GF(p)  # Конечное поле GF(p)
E = EllipticCurve(F, [a, b])  # Эллиптическая кривая y^2 = x^3 + ax + b

# Подсчитываем количество точек на кривой
num_points = E.cardinality()

# Вывод результата
print(f"Количество точек на кривой: {num_points}")
